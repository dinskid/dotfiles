DATA SEGMENT
M DB 3
N DB 3
O DB 3
A DB 1,2,3,4,5,6,7,8,9
B DB 1,0,0,0,1,0,0,0,1
C DB 9 DUP(0)
TEMP DB ?
DATA ENDS

CODE SEGMENT
ASSUME CS:CODE,DS:DATA
START:
MOV AX, DATA
MOV DS, AX
MOV BL, 0
LOOP1: CMP BL, M
	JGE ENDLOOP1
	MOV CL, 0
	LOOP2: CMP CL, O
		JGE ENDLOOP2
		MOV DL, 0
		LOOP3: CMP DL, N
			JGE ENDLOOP3
			MOV AL, N
			MUL BL
			ADD AL, DL
			MOV SI, AX
			MOV AL, A[SI]
			MOV TEMP, AL
			MOV AL, O
			MUL DL
			ADD AL, CL
			MOV SI, AX
			MOV AL, B[SI]
			MUL [TEMP]
			MOV TEMP, AL
			MOV AL, O
			MUL BL
			ADD AL, CL
			MOV SI, AX
			MOV AL, [TEMP]
			ADD C[SI], AL
			INC DL
			JMP LOOP3
		ENDLOOP3: INC CL
		JMP LOOP2
	ENDLOOP2: INC BL
	JMP LOOP1
ENDLOOP1: MOV AH, 4CH
INT 21H
CODE ENDS
END START
